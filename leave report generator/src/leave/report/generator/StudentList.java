class ButtonEditor extends DefaultCellEditor {

    private String label;
    private boolean isPushed;
    private boolean isUpdateButton;
    private int row;
    private JTable table;
    
    public ButtonEditor(JCheckBox checkBox, boolean isUpdateButton, JTable table) {
        super(checkBox);
        this.isUpdateButton = isUpdateButton;
        this.table = table;
    }

    @Override
    public Component getTableCellEditorComponent(JTable table, Object value, boolean isSelected, int row, int column) {
        this.row = row;
        label = (value == null) ? "" : value.toString();
        JButton button = new JButton(label);
        button.setBackground(isUpdateButton ? new Color(0, 102, 204) : new Color(255, 51, 51)); // Blue for Update, Red for Delete
        button.setForeground(Color.WHITE); // Set text color to white
        button.setMargin(new Insets(2, 5, 2, 5)); // Adding margin for button

        button.addActionListener(e -> fireEditingStopped());
        isPushed = true;
        return button;
    }

    @Override
    public Object getCellEditorValue() {
        if (isPushed) {
            if (!isUpdateButton) {
                // Handle delete action
                String rollno = (String) table.getValueAt(row, 0);
                int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "Delete", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    try {
                        Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/jhansi", "root", "First1002@d");
                        Statement statement = c.createStatement();
                        String sql = "DELETE FROM collegestudent WHERE Rollno = '" + rollno + "'";
                        statement.executeUpdate(sql);
                        ((DefaultTableModel) table.getModel()).removeRow(row);
                    } catch (SQLException e) {
                        e.printStackTrace();
                    }
                }
            } else {
                // Handle update action
                String rollno = (String) table.getValueAt(row, 0);
                String name = (String) table.getValueAt(row, 1);
                String year = (String) table.getValueAt(row, 2);
                String degree = (String) table.getValueAt(row, 3);
                String course = (String) table.getValueAt(row, 4);
                String gender = (String) table.getValueAt(row, 5);
                String guardian = (String) table.getValueAt(row, 6);
                String address = (String) table.getValueAt(row, 7);

                AddStudent addStudent = new AddStudent();
                addStudent.setStudentData(rollno, name, year, degree, course, gender, guardian, address);
                addStudent.setVisible(true);
                
                Window win = SwingUtilities.getWindowAncestor(table);
            if (win != null) {
                win.dispose();
            }
            }
        }
        isPushed = false;
        return label;
    }

    @Override
    public boolean stopCellEditing() {
        isPushed = false;
        return super.stopCellEditing();
    }
}

