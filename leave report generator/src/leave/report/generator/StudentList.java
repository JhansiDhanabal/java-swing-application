    private void loadData() throws SQLException {
        table.setGridColor(new java.awt.Color(204, 204, 204));
        table.setSelectionBackground(new java.awt.Color(153, 153, 153));
        table.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 12)); // Header font
        table.getTableHeader().setBackground(new Color(51, 0, 102)); // Header background color
        jScrollPane1.setViewportView(table);
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.getDataVector().removeAllElements();
        table.getColumn("Update").setCellEditor(new ButtonEditor(new JCheckBox(), true, table));
        table.getColumn("Delete").setCellEditor(new ButtonEditor(new JCheckBox(), false, table));
        try {
            Connection c = DriverManager.getConnection("jdbc:mysql://localhost:3306/jhansi","root","First1002@d");
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery("Select * from collegestudent");

            while (rs.next()) {
                String s1 = rs.getString("Rollno");
                String s2 = rs.getString("Name");
                String s3 = rs.getString("Year");
                String s4 = rs.getString("Degree");
                String s5 = rs.getString("Course");
                String s6 = rs.getString("Gender");
                String s7 = rs.getString("Gaurdian");
                String s8 = rs.getString("Address");

                model.addRow(new Object[]{s1, s2, s3, s4, s5, s6, s7, s8, "Update", "Delete"});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
